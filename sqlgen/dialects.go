package sqlgen

import "strconv"

type postgres struct{}

func Postgres() Dialect {
	return &postgres{}
}

func (p *postgres) Rebind(sql string) string {
	out := make([]byte, 0, len(sql)+10)

	j := 1
	for i := 0; i < len(sql); i++ {
		ch := sql[i]
		if ch != '?' {
			out = append(out, ch)
			continue
		}

		out = append(out, '$')
		out = append(out, strconv.Itoa(j)...)
		j++
	}

	return string(out)
}

type sqlite3 struct{}

func SQLite3() Dialect {
	return &sqlite3{}
}

func (s *sqlite3) Rebind(sql string) string {
	return sql
}
